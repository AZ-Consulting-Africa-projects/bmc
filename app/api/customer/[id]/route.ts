

import {Dto} from "@/DTO/Dto";
import { CustomerModel } from "@/models/modelCommercial/CustomerModel";
import prismadb from "@/prisma/prismadb";
import {NextResponse} from "next/server";

export async function PUT(req: Request, { params  }: { params: {id: string}}) {
    try {
        const id = Number(params.id);
        const body = await req.json();
        const customerModel: CustomerModel = body;

     

        const dataValidate = Dto.customerDto().validate(customerModel);
        if (dataValidate.error) {
            return  NextResponse.json({msg: dataValidate.error.message,  status: 422, ok:false });
        }
        else {
             const customer = await prismadb.customer.update({
            where: { id: Number(id) },
            data: dataValidate.value
        });

        return NextResponse.json({customer: customer, status: 200, ok:true });
        }

       
    } catch (error) {
        console.error('[updateUser]', error);
        return  NextResponse.json({msg:`Internal error ${error}`,  status: 500, ok: false });
    }
}

export async function DELETE(req: Request, { params  }: { params: {id: string}}) {
    try {
        const id = Number(params.id);
        const customer = await prismadb.customer.delete({
            where: { id: id }
        });

        return NextResponse.json({customer: customer, status: 200, ok:true });
    } catch (error) {
        console.error('[deleteUser]', error);
        return  NextResponse.json({msg: `Internal error ${error}`,  status: 500, ok:false });
    }
}

export async function GET(req: Request, { params  }: { params: {id: string}}) {
    try {
        const id = Number(params.id);
        const customer = await prismadb.customer.findUnique({
            where: { id }
        });

        return NextResponse.json(customer);
    } catch (error) {
        console.error('[deleteUser]', error);
        return  NextResponse.json({msg: `Internal error ${error}`,  status: 500 });
    }
}